def security_logs(request):
    """
    Reads the text reports generated by Bandit and the Fuzzer
    and displays them on the web page.
    """
    # Define paths to the reports (assumes they are in the project root)
    base_dir = settings.BASE_DIR
    bandit_path = os.path.join(base_dir, 'bandit_report.txt')
    fuzzer_path = os.path.join(base_dir, 'fuzzer_report.txt')
    
    reports = {}
    
    # Read Bandit Report
    if os.path.exists(bandit_path):
        with open(bandit_path, 'r') as f:
            reports['bandit'] = f.read()
    else:
        reports['bandit'] = "Report not found. Run: bandit -r . -f txt -o bandit_report.txt"

    # Read Fuzzer Report
    if os.path.exists(fuzzer_path):
        with open(fuzzer_path, 'r') as f:
            reports['fuzzer'] = f.read()
    else:
        reports['fuzzer'] = "Report not found. Run: python3 fuzzer.py > fuzzer_report.txt"

    return render(request, 'polls/security_logs.html', {'reports': reports})

