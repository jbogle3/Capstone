def upload_analysis(request):
    if request.method == 'POST':
        form = UploadFileForm(request.POST, request.FILES)
        if form.is_valid():
            instance = form.save(commit=False)
            uploaded_file = request.FILES['file']
            instance.file_name = uploaded_file.name
            
            # 1. SHA-256 Hash
            sha256_hash = hashlib.sha256()
            for chunk in uploaded_file.chunks():
                sha256_hash.update(chunk)
            instance.sha256_hash = sha256_hash.hexdigest()
            
            # Save initially to get file path
            instance.save()
            
            # 2. Analysis Logic
            try:
                file_path = instance.file.path
                pe = pefile.PE(file_path)
                
                # Reset flags first (assume False)
                instance.has_aslr = False
                instance.has_dep = False

                if pe.OPTIONAL_HEADER.DllCharacteristics & 0x0040:
                    instance.has_aslr = True
                
                if pe.OPTIONAL_HEADER.DllCharacteristics & 0x0100:
                    instance.has_dep = True
                
                pe.close()

                # --- CRITICAL: Mark UNSAFE if missing protections ---
                if not instance.has_aslr or not instance.has_dep:
                    instance.is_safe = False
                else:
                    instance.is_safe = True
                # ----------------------------------------------------

            except Exception as e:
                print(f"Analysis Error: {e}")
                # Optional: If it's not a PE file, is it safe? 
                # Let's say False to be cautious, or True if you only care about PE files.
                instance.is_safe = False 
            
            instance.save()
            return redirect('dashboard')
    else:
        form = UploadFileForm()
    
    return render(request, 'polls/upload_page.html', {'form': form})
    
